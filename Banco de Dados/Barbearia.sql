CREATE DATABASE BARBEARIA;
USE BARBEARIA;

CREATE TABLE CLIENTE(
    ID_CLIENTE INT AUTO_INCREMENT PRIMARY KEY,
    -- PRINCIPAL
    NOME VARCHAR(100) UNIQUE NOT NULL,
    CPF VARCHAR(14) UNIQUE NOT NULL,
    GENERO CHAR(1),
    -- CONTATO
    TELEFONE VARCHAR(20),
    EMAIL VARCHAR(100),
    -- ENDERECO
    ENDERECO VARCHAR(2),
    NUMERO VARCHAR(10),
    TIPO_RESIDENCIA(20),
    CEP VARCHAR(10)
);

CREATE TABLE SERVICO(
    ID_SERVICO_AGENDA INT AUTO_INCREMENT PRIMARY KEY,
    -- PRINCIPAL
    NOME_SERVICO VARCHAR(100),
    CUSTO VARCHAR(20)
);

CREATE TABLE BARBEIRO(
    ID_BARBEIRO INT AUTO_INCREMENT PRIMARY KEY,
    -- PRINCIPAL
    NOME VARCHAR(100) NOT NULL,
    CPF VARCHAR(14) UNIQUE NOT NULL,
    -- CONTATO
    TELEFONE VARCHAR(20),
    EMAIL VARCHAR(100) UNIQUE
);

CREATE TABLE AGENDA(
    ID_AGENDA INT AUTO_INCREMENT PRIMARY KEY,
    -- PRINCIPAL
    ID_CLIENTE INT,
    ID_BARBEIRO INT,
    -- AGENDA
    DATA_AGENDA DATE,
    HORA_AGENDA TIME,
    SITUACAO VARCHAR(20),
    -- SERVIÇO
    ID_SERVICO INT,
    -- REFERENCIAS
    FOREIGN KEY (ID_BARBEIRO) REFERENCES BARBEIRO(ID_BARBEIRO),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE)
    FOREIGN KEY (ID_SERVICO) REFERENCES SERVICO(ID_SERVICO_AGENDA)
);

